public class ShortHandOperator {

	public static void main(String[] args) {
		
		
	int	i= 10;
	System.out.println("the score is "+i);
	
	//i=i+5;
	i+=5; //shorthand operator has no space in between
	
	
	
	System.out.println("the score is "+i);
	//i=i-2;
	i-=2;
	
			
	System.out.println("the score is "+i);
	
	//i=i*10;
	i*=10;
	
	System.out.println("the score is "+i);
	//i=i/5;
	i/=5;
	
	System.out.println("the score is "+i);
	//i=i%7;
	i%=7;
	
			
	System.out.println("the score is "+i);
	
	
	
			
			
	
		

	}

}
