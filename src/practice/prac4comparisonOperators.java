package practice;

public class prac4comparisonOperators {

	public static void main(String[] args) {
		
		int n1 = 50;
		int n2 = 55;
		
		System.out.println(n1>n2);
        System.out.println(n1<n2);
        System.out.println(n1<=n2);
        System.out.println(n1>=n2);
        System.out.println(n1==n2);
        System.out.println(n1!=n2);
        
	}

}

